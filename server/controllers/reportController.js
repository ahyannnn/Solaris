// Helper function to get financial data - ONLY 6 columns
const getFinancialData = async (queryParams) => {
  const { startDate, endDate } = queryParams;

  let dateFilter = {};
  if (startDate && endDate) {
    const start = new Date(startDate);
    const end = new Date(endDate);
    if (!isNaN(start.getTime()) && !isNaN(end.getTime())) {
      dateFilter = { createdAt: { $gte: start, $lte: end } };
    }
  }

  // Get pre-assessments with payments
  const preAssessments = await PreAssessment.find({
    ...dateFilter,
    paymentStatus: { $in: ['paid', 'for_verification'] }
  }).populate('clientId', 'contactFirstName contactLastName');

  // Get projects with payments
  const projects = await Project.find({
    ...dateFilter,
    amountPaid: { $gt: 0 }
  }).populate('clientId', 'contactFirstName contactLastName');

  const payments = [];

  // Pre-assessment payments
  preAssessments.forEach(pa => {
    payments.push({
      reference: pa.bookingReference || 'N/A',
      clientName: `${pa.clientId?.contactFirstName || ''} ${pa.clientId?.contactLastName || ''}`.trim() || 'N/A',
      amount: pa.assessmentFee || 0,
      paymentMethod: pa.paymentGateway === 'paymongo' ? 'PayMongo' : (pa.paymentMethod || 'Manual').toUpperCase(),
      status: pa.paymentStatus === 'paid' ? 'Paid' : 'For Verification',
      date: pa.paymentCompletedAt || pa.confirmedAt || pa.createdAt
    });
  });

  // Project payments
  projects.forEach(p => {
    let status = 'In Progress';
    if (p.status === 'completed') status = 'Completed';
    else if (p.status === 'full_paid') status = 'Full Payment Received';

    payments.push({
      reference: p.projectReference || 'N/A',
      clientName: `${p.clientId?.contactFirstName || ''} ${p.clientId?.contactLastName || ''}`.trim() || 'N/A',
      amount: p.amountPaid || 0,
      paymentMethod: 'Manual',
      status: status,
      date: p.startDate || p.createdAt
    });
  });

  payments.sort((a, b) => new Date(b.date) - new Date(a.date));

  return {
    title: 'Financial Report',
    generatedAt: new Date(),
    dateRange: { startDate, endDate },
    companyInfo: COMPANY_INFO,
    payments: payments
  };
};

