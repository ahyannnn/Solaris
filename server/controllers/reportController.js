const getExcelColumnName = (number) => {
  let column = "";

  while (number > 0) {
    const remainder = (number - 1) % 26;
    column = String.fromCharCode(65 + remainder) + column;
    number = Math.floor((number - 1) / 26);
  }

  return column;
};
// Helper function to add company header to PDF
const addCompanyHeaderToPDF = (doc) => {
  // Company Name
  const { left, right } = doc.page.margins;
  const width = doc.page.width - left - right;
  doc.save();
  doc.fillColor(REPORT_THEME.navy).font('Helvetica-Bold').fontSize(17)
    .text(COMPANY_INFO.name, left, 27, { width, align: 'center', lineBreak: false });
  doc.font('Helvetica').fontSize(8.5).fillColor(REPORT_THEME.muted)
    .text(COMPANY_INFO.address, left, 50, { width, align: 'center', lineBreak: false });
  doc.font('Helvetica-Oblique').fontSize(8).fillColor(REPORT_THEME.blue)
    .text(COMPANY_INFO.tagline, left, 64, { width, align: 'center', lineBreak: false });
  doc.strokeColor(REPORT_THEME.blue).lineWidth(2).moveTo(left, 83).lineTo(left + width, 83).stroke();
  doc.restore();
  doc.y = 105;
};

const addPdfFooter = (doc) => {
  const y = doc.page.height - 70;
  doc.save();
  doc.strokeColor(REPORT_THEME.border).lineWidth(0.5)
    .moveTo(50, y - 8).lineTo(doc.page.width - 50, y - 8).stroke();
  doc.fillColor(REPORT_THEME.muted).font('Helvetica').fontSize(7)
    .text(`Generated by ${COMPANY_INFO.name}`, 50, y, { width: doc.page.width - 100, align: 'center', lineBreak: false });
  doc.restore();
};

const drawPdfTable = (doc, headers, rows, columnWidths) => {
  const left = 50;
  const width = doc.page.width - 100;
  const rowHeight = 23;
  const drawHeader = (y) => {
    let x = left;
    doc.save().fillColor(REPORT_THEME.blue).rect(left, y, width, rowHeight).fill();
    doc.fillColor('#FFFFFF').font('Helvetica-Bold').fontSize(7.5);
    headers.forEach((header, index) => {
      doc.text(header, x + 5, y + 7, { width: columnWidths[index] - 10, lineBreak: false });
      x += columnWidths[index];
    });
    doc.restore();
  };
  let y = doc.y;
  drawHeader(y);
  y += rowHeight;
  rows.forEach((row, rowIndex) => {
    if (y + rowHeight > doc.page.height - 58) {
      addPdfFooter(doc);
      doc.addPage();
      addCompanyHeaderToPDF(doc);
      y = doc.y;
      drawHeader(y);
      y += rowHeight;
    }
    let x = left;
    doc.save();
    doc.fillColor(rowIndex % 2 ? '#FFFFFF' : REPORT_THEME.lightBlue).rect(left, y, width, rowHeight).fill();
    doc.strokeColor(REPORT_THEME.border).lineWidth(0.35).rect(left, y, width, rowHeight).stroke();
    doc.fillColor('#1F2D38').font('Helvetica').fontSize(7.3);
    row.forEach((value, index) => {
      doc.text(String(value || '—'), x + 5, y + 7, { width: columnWidths[index] - 10, lineBreak: false, ellipsis: true });
      x += columnWidths[index];
    });
    doc.restore();
    y += rowHeight;
  });
  doc.y = y;
};

